# arithmetic operators
a = 3
b = 4
c = a+b
print("the addition value of a and b is: ",c)
c = a-b
print("the subtraction value of a and b is: ",c)
c = a*b
print("the multiplication value of a and b is: ",c)
c = a/b
print("the division value of a and b is: ",c)

# assignment operators
a = 34
a += 12
a -= 12
a *= 12
a /= 12
print(a)

# comparison operators
b = (14<7)
b = (14>7)
b = (14>=7)
b = (14<=7)
b = (14==7)
b = (14!=7)
print(b)

# logical operators
bool1 = True
bool2 = False
print("the value of bool1 and bool2 is", (bool1 and bool2))
print("the value of bool1 or bool2 is", (bool1 or bool2))
print("the value of not bool2 is", (not bool2))