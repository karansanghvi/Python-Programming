# use comparison operators to check whether a is greater than b or not
a = 34
b = 80
print("value when a is greater than b: ",a>b)
print("value when a is less than b: ",a<b)
print("value when a is greater than equal to b: ",a>=b)
print("value when a is less than equal to b: ",a<=b)
print("value when a is equal to b: ",a==b)
print("value when a is not equal to b: ",a!=b)
