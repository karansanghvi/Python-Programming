story = "once upon a time there was a developer named karan who was a pro at web dev"
print(len(story)) #this shows that the length of the string that how many characters are there
print(story.endswith("dev")) #it shows that whether dev is ending with the string story or no
print(story.count("a")) #to find how many a's are in the string
print(story.count("was a")) #to count the number of characters in the string
print(story.capitalize()) #to capitalize the string letter in the string
print(story.find("karan")) #whether the character is there in the string
print(story.replace("karan", "karan sanghvi")) #to replace karan with karan sanghvi